var (
	ref string
	help bool
)

func main() {

	flag.StringVar(&ref, "ref", "", "(optional) the reference to retrieve.")
	flag.BoolVar(&help, "help", false, "(optional) trigger the help")
	flag.Parse()

	if help {
		flag.PrintDefaults()
		return
	}

	if ref == "" {
		week := diff()
		ref = Base[week]
	}

	fmt.Println("")
	fmt.Println(string(ping(ref)))
	fmt.Println("")
}

func ping(ref string) []byte {

	vals := &url.Values{}
	vals.Set("passage", ref)
	vals.Set("key", "IP")
	vals.Set("output-format", "plain-text")
	vals.Set("include-short-copyright", "0")
	vals.Set("include-copyright", "0")
	vals.Set("include-audio-link", "0")
	vals.Set("include-word-ids", "0")
	vals.Set("include-verse-ids", "0")
	vals.Set("include-headings", "0")
	vals.Set("include-subheadings", "0")
	vals.Set("include-footnote-links", "0")
	vals.Set("include-footnotes", "0")
	vals.Set("include-verse-numbers", "1")
	vals.Set("include-passage-references", "1")

	url, err := url.Parse("http://www.esvapi.org/v2/rest/passageQuery")
	if err != nil {
		log.Fatal(err)
	}

	url.RawQuery = vals.Encode()

	req, err := http.NewRequest("GET", url.String(), nil)

	// req.Header.Set("", "")

	res, err := (&http.Client{}).Do(req)
	if err != nil {
		log.Fatal(err)
	}

	content, err := ioutil.ReadAll(res.Body)
	if err != nil {
		log.Fatal(err)
	}

	res.Body.Close()
	return content

}

func diff() int {
	// Mon, 02 Jan 2006 15:04:05 -0700
	format := "Jan 2, 2006"
	t, err := time.Parse(format, "Jan 10, 2011")
	if err != nil {
		log.Fatal(err)
	}
	return int(math.Floor(time.Since(t).Hours() / 24 / 7))
}

